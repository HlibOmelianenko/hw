fn main() {
    // Константи для визначення розміру ромба
    const HEIGHT: usize = 5;  // Висота верхньої частини ромба
    const WIDTH: usize = 9;   // Ширина ромба (для повного ромба)

    // Малювання верхньої частини ромба
    for i in 0..HEIGHT {
        let spaces = HEIGHT - i - 1;
        let stars = 2 * i + 1;

        // Виведення пробілів і зірочок в одному рядку
        print!("{:width$}", "", width = spaces);  // Виводимо пробіли з відповідною шириною
        println!("{}", "*".repeat(stars));       // Виводимо зірочки
    }

    // Малювання нижньої частини ромба
    for i in (0..HEIGHT-1).rev() {
        let spaces = HEIGHT - i - 1;
        let stars = 2 * i + 1;

        // Виведення пробілів і зірочок в одному рядку
        print!("{:width$}", "", width = spaces);  // Виводимо пробіли з відповідною шириною
        println!("{}", "*".repeat(stars));       // Виводимо зірочки
    }
}
